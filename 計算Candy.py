#將給定金幣計算出可以兌換最多的糖果數,規則如下:
#a. 1 塊金幣換 1 顆糖果,
#b. 1 顆糖果有 1 張包裝紙,
#c. 3 張包裝紙可以換 1 顆糖果,
#如:3 塊金幣可換 4 顆糖果
#輸入:第一行資料為測試資料之筆數,第二行之後為測試資料,
#輸出:對每一組測試資料,輸出可換的糖果數。
#輸入範例: 輸出範例:
#2 
#3       4
#15      22

N =eval(input())
for i in range(N):
    G =int(input())
    NoP = G
    NoC = G
    while NoP >= 3:
        CT = NoP // 3
        NoP -= (CT * 3)
        NoC += CT
        NoP += CT
    print(NoC)